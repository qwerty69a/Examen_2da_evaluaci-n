package Figuras;
public class Punto
{
    private int x, y;
    Punto()
    {
        setX(x);
        setY(y);
    }
    public int getX()
    {
        return x;
    }
    public void setX(int x)
    {
        this.x = x;
    }
    public int getY()
    {
        return y;
    }
    public void setY(int y)
    {
        this.y = y;
    }
}