public class Animal
{
    private int ojos, extremidades;
    private String tipoPiel, reproduccion, mueven;
    public int getOjos()
    {
        return ojos;
    }
    public void setOjos(int ojos)
    {
        this.ojos = ojos;
    }
    public int getExtremidades()
    {
        return extremidades;
    }
    public void setExtremidades(int extremidades)
    {
        this.extremidades = extremidades;
    }
    public String getReproduccion()
    {
        return reproduccion;
    }
    public void setReproduccion(String reproduccion)
    {
        this.reproduccion = reproduccion;
    }
    public String getMueven()
    {
        return mueven;
    }
    public void setMueven(String mueven)
    {
        this.mueven = mueven;
    }
    public String getTipoPiel()
    {
        return tipoPiel;
    }
    public void setTipoPiel(String tipoPiel)
    {
        this.tipoPiel = tipoPiel;
    }
}